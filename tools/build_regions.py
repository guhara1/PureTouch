def pick(pool, key, n=1):
    """동 슬러그 해시로 변형을 안정적으로 선택 (재생성해도 같은 결과)."""
    h = int(hashlib.md5(key.encode()).hexdigest(), 16)
    if n == 1:
        return pool[h % len(pool)]
    out, idx = [], h
    p = list(pool)
    while p and len(out) < n:
        out.append(p.pop(idx % len(p)))
        idx //= 7 or 1
        idx += 3
    return out


