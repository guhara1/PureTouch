    base = city["path"] + ((gu["slug"] + "/") if gu else "") + dn + "/"
    loc = f"{cname} {guname}" if guname else cname
    key = base

    feats = []
    for t in dong["tags"][:2]:
        feats.append(pick(FEAT[t], key + t).format(d=d))
