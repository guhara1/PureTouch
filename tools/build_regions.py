def build_all():
    pages = []
    # 충청 3개 시
    for city in CITIES:
        if city["gus"]:
            for gu in city["gus"]:
                pages.append(gu_page(city, gu))
                base = city["path"] + gu["slug"] + "/"
                for d in gu["dongs"]:
                    sibs = [(s["name"], base + s["slug"] + "/") for s in gu["dongs"] if s["slug"] != d["slug"]]
                    pages.append(dong_page(city, gu, d, sibs))
        else:
            base = city["path"]
            for d in city["dongs"]:
                sibs = [(s["name"], base + s["slug"] + "/") for s in city["dongs"] if s["slug"] != d["slug"]]
                pages.append(dong_page(city, None, d, sibs))
    # 호남 시들
    for city in HONAM_CITIES:
        city["honam"] = True
        pages.append(honam_city_page(city))
        if city.get("gus"):
            for gu in city["gus"]:
                pages.append(gu_page(city, gu))
                base = city["path"] + gu["slug"] + "/"
                for d in gu["dongs"]:
                    sibs = [(s["name"], base + s["slug"] + "/") for s in gu["dongs"] if s["slug"] != d["slug"]]
                    pages.append(dong_page(city, gu, d, sibs))
        else:
            base = city["path"]
            for d in city["dongs"]:
                sibs = [(s["name"], base + s["slug"] + "/") for s in city["dongs"] if s["slug"] != d["slug"]]
                pages.append(dong_page(city, None, d, sibs))
    return pages


